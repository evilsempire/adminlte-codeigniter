    /**
     * @desc placeholder image will create a placeholder image for user if the image is not uploaded by user.
     * @param type $string
     * @return type string img src
     */
    public function placeholder_image($string){
        $colors = '';
        for($o=1;$o<=3;$o++)
        {
            $colors .= str_pad( dechex( mt_rand( 100, 150 ) ), 2, '0', STR_PAD_LEFT);
        }
       

//        $k = array_rand($colors);
//        $v = $colors[$k];
        $url = 'https://via.placeholder.com/250/' . $colors. '/fff?text=';
        $str_to_array = explode(" ", $string);
        if (count($str_to_array) >= 2):
            $a = ucwords(substr("$str_to_array[0]", 0, 1));
            $b = ucwords(substr("$str_to_array[1]", 0, 1));
            return $url. $a . $b;
        elseif (count($str_to_array) == 1):
            return $url.ucwords(substr("$str_to_array[0]", 0, 1));
        endif;
    }


   
