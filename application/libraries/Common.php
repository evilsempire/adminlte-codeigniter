        /**
         * 
         * @param type $tableData  //tableddata for sorting and searching
         * @param type $table      //table id 
         * @param type $init_sql   //this variable will act as a primary sql if join or any other conditions are present
         * @return type array 
         */
        public function Table($tableData,$table,$init_sql = ""){
        
        $sql = "";
        $records_per_page = isset($tableData['rowCount'])? $tableData['rowCount']: 10;              //ger the records per page
        $current_page_number  = isset($tableData['current'])? $tableData['current'] : 1;            //get the current page if set
        
        $start_from = ($current_page_number - 1) * $records_per_page;                               //get the start from
        
        $sql .= ($init_sql != "")? $init_sql: "SELECT * FROM ".$table ." WHERE deleted = 0 " ;
        
        $order_by = '';
        if(isset($tableData["sort"]) && is_array($tableData["sort"]))
        {
            foreach($tableData["sort"] as $key => $value)
            {
             $order_by .= " $key $value, ";
            }
        }
        else
        {
            $sql .= ' ORDER BY '. $table .'.id ASC';
        }
        
        if($order_by != '')
        {
         $sql .= ' ORDER BY ' . substr($order_by, 0, -2);
        }
        if($records_per_page != -1)
        {
         $sql .= " LIMIT " . $start_from . ", " . $records_per_page;
        }
        
            $query = $this->CI->db->query($sql);
        return $query->result_array();
    }
    
    
